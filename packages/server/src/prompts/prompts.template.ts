export function buildStrategyPrompt(data: {
  baziText: string;
  wuxingText: string;
  userGoal?: string;
}): string {
  return `
## 生成个性化改命策略

八字排盘：
${data.baziText}

五行分析：
${data.wuxingText}

${data.userGoal ? `\n用户当前目标：${data.userGoal}\n` : ''}

## 输出要求
生成3条改命策略，每条必须包含：
1. 策略名称（简洁）
2. 具体行动步骤
3. 最佳执行时间（精确到时辰）
4. 最佳方位
5. 所需物品（如有）
6. 预期效果周期
7. 原理说明（道之光风格）

优先针对用神${data.wuxingText.includes('用神') ? '' : ''}进行补益。
`;
}

