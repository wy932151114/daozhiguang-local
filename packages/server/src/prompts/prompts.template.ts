// 2. 命理结果注入模板（AI改写用）
// ============================================================

export function buildFortunePrompt(data: {
  baziText?: string;
  wuxingText?: string;
  jiugongText?: string;
  dayunText?: string;
  shenshaText?: string;
  userQuestion?: string;
}): string {
  return `
## 当前用户命理数据

${data.baziText ? `【八字排盘】\n${data.baziText}\n` : ''}
${data.wuxingText ? `【五行分析】\n${data.wuxingText}\n` : ''}
${data.jiugongText ? `【九宫飞星】\n${data.jiugongText}\n` : ''}
${data.dayunText ? `【大运流年】\n${data.dayunText}\n` : ''}
${data.shenshaText ? `【神煞】\n${data.shenshaText}\n` : ''}
${data.userQuestion ? `\n## 用户问题\n${data.userQuestion}\n` : ''}

## 输出要求

请基于以上命理数据，生成完整的改命建议。
格式要求：
1. 先整体分析当前命局状态（200字以内）
2. 给出3-5条可操作的改命策略，每条包含6要素
3. 一个今日小行动（具体到做什么、什么时候做）
4. 以一段道之光风格的金句收尾

## 约束
- 禁止自创排盘数据，严格基于输入数据
- 必须给具体行动方案，不能只说"随缘"
- 引用经典必须真实可靠
`;
}

